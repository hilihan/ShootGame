package cn.mark;

import java.awt.image.BufferedImage;

/**
 * 飞行物父类
 * 所有的飞行物都具有以下属性和方法
 * @author hilih
 */
public class FlyingObject {
	protected int x;    //x坐标
	protected int y;    //y坐标
	protected int width;    //宽
	protected int height;   //高
	protected BufferedImage image;   //图片 BufferedImage是Java中用来保存图片的一个类
	
}
